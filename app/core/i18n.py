def current_language() -> str:
    return AppSettings.current().language


def tr(key: str, *, lang: str | None = None) -> str:
    language = lang or current_language()
    bucket = _MESSAGES.get(language) or _MESSAGES["en"]
    return bucket.get(key) or _MESSAGES["en"].get(key, key)


def nav_label(route_key: str, *, lang: str | None = None) -> str:
    return tr(f"nav.{route_key}", lang=lang)
